def generate_differential(
    symptoms: str,
    patient_info: str = "",
    exam_findings: str = "",
    conversation_history: Optional[List[Dict[str, str]]] = None,
) -> str:
    """Generate ranked differential diagnoses using the LLM.

    Args:
        symptoms: Free-text description of presenting symptoms.
        patient_info: Patient demographics, past medical history, medications.
        exam_findings: Physical examination findings.
        conversation_history: Optional prior messages for multi-turn reasoning.

    Returns:
        LLM response with ranked differential diagnoses (educational only).
    """
    if conversation_history is None:
        conversation_history = []

    clinical_prompt = f"""Please generate a ranked differential diagnosis for the following clinical presentation:

**Presenting Symptoms:** {symptoms}
"""
    if patient_info:
        clinical_prompt += f"\n**Patient Information:** {patient_info}"
    if exam_findings:
        clinical_prompt += f"\n**Examination Findings:** {exam_findings}"

    clinical_prompt += """

Please provide:
1. A ranked list of differential diagnoses from most to least likely.
2. For each diagnosis, explain the supporting and opposing evidence.
3. Identify any red-flag features requiring urgent attention.
4. Suggest initial workup to differentiate between the top diagnoses.

Remember: This is for EDUCATIONAL purposes only."""

    messages = [{"role": "system", "content": SYSTEM_PROMPT}]
    messages.extend(conversation_history)
    messages.append({"role": "user", "content": clinical_prompt})

    logger.info("Sending differential diagnosis query to LLM (%d prior messages)", len(conversation_history))

    try:
        response = chat(messages)
        logger.info("Received LLM response (%d chars)", len(response))
        return response
    except Exception as exc:
        logger.error("LLM call failed: %s", exc)
        raise


