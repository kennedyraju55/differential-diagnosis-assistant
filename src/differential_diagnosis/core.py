class DiagnosisSession:
    """Track diagnostic reasoning across a session."""

    def __init__(self) -> None:
        """Initialize the diagnosis session."""
        self.entries: List[Dict[str, Any]] = []

    def add_entry(
        self,
        symptoms: str,
        patient_info: str,
        exam_findings: str,
        urgency: int,
        systems: List[str],
        response: str,
    ) -> None:
        """Add a diagnostic entry to the session history."""
        entry = {
            "timestamp": datetime.now().isoformat(),
            "symptoms": symptoms,
            "patient_info": patient_info,
            "exam_findings": exam_findings,
            "urgency": urgency,
            "systems": systems,
            "response": response,
        }
        self.entries.append(entry)
        logger.info("Added session entry (total: %d)", len(self.entries))

    def get_history(self) -> List[Dict[str, Any]]:
        """Get all session entries."""
        return list(self.entries)

    def get_summary(self) -> Dict[str, Any]:
        """Get a summary of the diagnostic session."""
        if not self.entries:
            return {
                "total_consultations": 0,
                "max_urgency": 0,
                "systems_involved": [],
                "all_symptoms": [],
            }

        all_systems: List[str] = []
        all_symptoms: List[str] = []
        max_urgency = 0

        for entry in self.entries:
            for s in entry.get("systems", []):
                if s not in all_systems:
                    all_systems.append(s)
            all_symptoms.append(entry["symptoms"])
            if entry["urgency"] > max_urgency:
                max_urgency = entry["urgency"]

        return {
            "total_consultations": len(self.entries),
            "max_urgency": max_urgency,
            "systems_involved": all_systems,
            "all_symptoms": all_symptoms,
        }


